<?php

namespace Src\Models;

class ModelTag
{
    private $id;
    private $text;

    public function __construct()
    {
    }

    public function getText()
    {
        return $this->text;
    }

    public function setText($text)
    {
        $this->text = $text;
    }

    public function getId()
    {
        return $this->id;
    }

    public function setId($id)
    {
        $this->id = $id;
    }
}