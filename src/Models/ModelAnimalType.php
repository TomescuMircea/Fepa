<?php

namespace Src\Models;

class ModelAnimalType
{
    private $id;
    private $type;

    public function __construct()
    {
    }

    public function getType()
    {
        return $this->type;
    }

    public function setType($type)
    {
        $this->type = $type;
    }

    public function getId()
    {
        return $this->id;
    }

    public function setId($id)
    {
        $this->id = $id;
    }
}